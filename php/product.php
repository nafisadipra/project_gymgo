<script >
// Cart functionality
let listCart = document.querySelector('.listCart');
let cartButtons = document.querySelectorAll('.cart');
let closebtn = document.querySelector('.cart_tab .close');
let body = document.body;
let total = 0;
let cartItems = [];

// Cart toggle functionality
cartButtons.forEach(cartb => {
    cartb.addEventListener('click', () => {
        body.classList.add('activeTabCart');
    });
});

closebtn.addEventListener('click', () => {
    body.classList.remove('activeTabCart');
});

function addToCart(product) {
    let existingItem = cartItems.find(item => item.id === product.id);
    
    if (existingItem) {
        existingItem.quantity++;
    } else {
        cartItems.push({
            id: product.id,
            name: product.name,
            price: product.price,
            image: product.image,
            quantity: 1
        });
    }
    
    updateCartDisplay();
    body.classList.add('activeTabCart');
}

function updateCartDisplay() {
    listCart.innerHTML = '';
    total = 0;
    
    cartItems.forEach(item => {
        total += item.price * item.quantity;
        
        let newDiv = document.createElement('div');
        newDiv.innerHTML = `
            <div class="cart-item">
                <img src="${item.image}" alt="${item.name}">
                <div class="content">
                    <h3>${item.name}</h3>
                    <p class="price">₹${item.price}/-</p>
                    <div class="quantity">
                        <button onclick="changeQuantity('${item.id}', ${item.quantity - 1})">-</button>
                        <span>${item.quantity}</span>
                        <button onclick="changeQuantity('${item.id}', ${item.quantity + 1})">+</button>
                    </div>
                </div>
            </div>
        `;
        listCart.appendChild(newDiv);
    });
    
    if (cartItems.length > 0) {
        let totalDiv = document.createElement('div');
        totalDiv.classList.add('total');
        totalDiv.innerHTML = `Total: ₹${total}/-`;
        listCart.appendChild(totalDiv);
    } else {
        listCart.innerHTML = '<div style="text-align: center; padding: 20px;">Your cart is empty</div>';
    }
}

function changeQuantity(id, newQuantity) {
    if (newQuantity <= 0) {
        cartItems = cartItems.filter(item => item.id !== id);
    } else {
        let item = cartItems.find(item => item.id === id);
        if (item) {
            item.quantity = newQuantity;
        }
    }
    updateCartDisplay();
}

// Add to cart button listeners
document.querySelectorAll('.preview .cart').forEach(button => {
    button.addEventListener('click', () => {
        const preview = button.closest('.preview');
        const product = {
            id: preview.getAttribute('data-target'),
            name: preview.querySelector('h4').textContent,
            price: parseInt(preview.querySelector('.price').textContent),
            image: preview.querySelector('img').src
        };
        addToCart(product);
    });
});

// Checkout functionality
document.querySelector('.checkout').addEventListener('click', () => {
    if (cartItems.length > 0) {
        alert(`Order placed! Total amount: ₹${total}/-`);
        cartItems = [];
        updateCartDisplay();
        body.classList.remove('activeTabCart');
    } else {
        alert('Your cart is empty!');
    }
});

</script>

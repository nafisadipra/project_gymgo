    <script>
        const lists = document.querySelectorAll('.list');
        const previews = document.querySelectorAll('.preview');
        const productPreview = document.querySelector('.product-preview');

        lists.forEach(list => {
            list.addEventListener('click', () => {
                const target = list.getAttribute('data-name');
                
                previews.forEach(preview => {
                    preview.classList.remove('active');
                });
                
                const selectedPreview = document.querySelector(`.preview[data-target="${target}"]`);
                selectedPreview.classList.add('active');
                
                productPreview.style.display = 'flex';
            });
        });

        // Close preview when clicking 'x'
        document.querySelectorAll('.fas.fa-times').forEach(closeBtn => {
            closeBtn.addEventListener('click', () => {
                productPreview.style.display = 'none';
            });
        });

        // Close preview when clicking outside
        productPreview.addEventListener('click', (e) => {
            if (e.target === productPreview) {
                productPreview.style.display = 'none';
            }
        });
    </script>
    
